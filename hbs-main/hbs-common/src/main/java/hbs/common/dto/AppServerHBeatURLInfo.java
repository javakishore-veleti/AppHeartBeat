package hbs.common.dto;

public class AppServerHBeatURLInfo {

	public String appName;

	private String appHeartBeatUrl;

	public AppServerHBeatURLInfo() {
	}

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public String getAppHeartBeatUrl() {
		return appHeartBeatUrl;
	}

	public void setAppHeartBeatUrl(String appHeartBeatUrl) {
		this.appHeartBeatUrl = appHeartBeatUrl;
	}

}
